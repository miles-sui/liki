#!/usr/bin/env node
// compile-vue-template.cjs — Pre-compile chat.html Vue template to render function.
// This eliminates the need for the Vue template compiler at runtime,
// allowing us to use vue.runtime.global.prod.js and remove unsafe-eval from CSP.

const fs = require('fs');
const path = require('path');
const { compile } = require('@vue/compiler-dom');

const WEB_DIR = path.resolve(__dirname, '..');

// Read chat.html and extract template inside #app.
const html = fs.readFileSync(path.join(WEB_DIR, 'chat.html'), 'utf-8');

// Extract content inside <div id="app">.
const bodyMatch = html.match(/<div\s[^>]*\bid="app"[^>]*>([\s\S]*?)<\/div>\s*<\/body>/);
if (!bodyMatch) {
  console.error('ERROR: could not find <div id="app"> in chat.html');
  process.exit(1);
}
let template = bodyMatch[1]
  .replace(/<script\b[^>]*>[\s\S]*?<\/script>/gi, '')
  .replace(/<style\b[^>]*>[\s\S]*?<\/style>/gi, '')
  .trim();

let result;
try {
  result = compile(template, {
    mode: 'function',
    hoistStatic: true,
    prefixIdentifiers: true,
  });
} catch (e) {
  console.error('Compilation error:', e.message);
  process.exit(1);
}

// The compiler emits: const {...} = Vue; ... return function render(...){...}
// We capture the return value as window.__chatAppRender.
const output = [
  '// Pre-compiled Vue render function for chat.html — generated by compile-vue-template.cjs',
  '// Do not edit manually.',
  'window.__chatAppRender = (function(){',
  result.code,
  '})();',
].join('\n');

const outPath = path.join(WEB_DIR, 'js', 'chat-template.js');
fs.writeFileSync(outPath, output);
console.log('Compiled chat template → js/chat-template.js (' + output.length + ' bytes)');
